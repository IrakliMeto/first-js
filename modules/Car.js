class Car {
  constructor(id, manufacturer, model, color, releaseYear, horsePower, price) {
    this.id = id;
    this.manufacturer = manufacturer;
    this.model = model;
    this.color = color;
    this.releaseYear = releaseYear;
    this.horsePower = horsePower;
    this.price = price;
  }
}

export default Car;
